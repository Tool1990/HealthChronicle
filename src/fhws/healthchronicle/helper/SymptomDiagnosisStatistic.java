package fhws.healthchronicle.helper;

public class SymptomDiagnosisStatistic
{
	private int percentage;
	private String symptomText;
	private String diagnosisText;

	public int getPercentage()
	{
		return percentage;
	}

	public void setPercentage(int percentage)
	{
		this.percentage = percentage;
	}

	public String getSymptomText()
	{
		return symptomText;
	}

	public void setSymptomText(String symptomText)
	{
		this.symptomText = symptomText;
	}

	public String getDiagnosisText()
	{
		return diagnosisText;
	}

	public void setDiagnosisText(String diagnosisText)
	{
		this.diagnosisText = diagnosisText;
	}
}
